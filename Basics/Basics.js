//increment operator

//i++ increments the one value to current value and prints the value before increment
let i=0;
console.log(i++);//prints the 0 this is before increment
console.log(i);//prints the 1 this is after increment

let age = 64;
age++;
console.log(age);//prints 65


//++j increments the one value to current value and prints the value after increment
let j=2;
console.log(++j);//prints the (2+1) 3 this is after increment
console.log(j);//prints the 3

let a = 5;
let result = a++;
console.log(result);//prints before increment value i.e 5 and a=6
result = ++a; // here a=6 and incremented by one
console.log(result)// prints 7

